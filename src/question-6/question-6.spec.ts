import { Integral } from './question-6';

describe('Integral', () => {
    const expressions: [
        exp: string,
        start: number,
        end: number,
        res: string,
    ][] = [
        ['3x^4+22.5x^3-4x', 0, 2, '101.20'],
        ['-4.5x^2+3', 8, 9, '-322.50'],
        ['2x', 0, 10, '100.00'],
        ['2x^4', -2, 3, '110.00'],
        ['2+4x^4', -1, 2, '32.40'],
        ['4.5x^2', 8, 9, '325.50'],
        ['-4', 0, 1, '-4.00'],
        ['-3x^1', -1, 1, '0.00'],
        ['-4x^3+0x^2-5', -1, 1, '-10.00'],
        ['6x^0', -1, 1, '12.00'],
        ['-5x^2-7x^4+8x^2-7', -1, 1, '-14.80'],
        ['-x^0+x^1+0x^3', -1, 1, '-2.00'],
        ['-8-8x^3+0x^4+3-6x^1-6x^1+9+7x^4', -1, 1, '10.80'],
        ['9x^3+10x^1-6x^1+8x^3+x^4-9x^1-4+4-4x^4', -1, 1, '-1.20'],
        ['4x^4-8x^4+8+2x^2-8-9x^1+6-3x^0-6x^4+x^2', -1, 1, '4.00'],
        ['4x^15+4', 0, 1, '4.25'],
        ['8x^49-2', 0, 1, '-1.84'],
    ];

    for (let i = 0; i < expressions.length; ++i) {
        const [exp, start, end, res] = expressions[i];
        it(`should work ${i + 1}`, () => {
            expect(Integral(exp, start, end)).toEqual(res);
        });
    }
});
